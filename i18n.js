function getLang() {
  try {
    const saved = localStorage.getItem(I18N_LANG_KEY);
    if (saved === 'az' || saved === 'en') return saved;
  } catch {}
  return 'en';
}

function interpolate(str, vars) {
  if (!vars || typeof vars !== 'object') return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? String(vars[k]) : `{${k}}`));
}

const LOCATION_I18N_KEYS = {
  'Main Building': 'locMainBuilding',
  Library: 'locLibrary',
  Cafeteria: 'locCafeteria',
  Lab: 'locLab',
  'Classroom Building': 'locClassroomBuilding',
  Dormitory: 'locDormitory',
  'Parking Area': 'locParkingArea',
  'Security Desk': 'locSecurityDesk',
  Other: 'locOther'
};

function formatLocation(value) {
  if (value == null || value === '') return '';
  const key = LOCATION_I18N_KEYS[value];
  return key ? t(key) : String(value);
}

function t(key, vars) {
  const lang = window.Lang?.current || getLang();
  const raw = I18N[lang]?.[key] ?? I18N.en[key] ?? key;
  return interpolate(raw, vars);
}

function setLang(lang) {
  const next = lang === 'az' ? 'az' : 'en';
  try { localStorage.setItem(I18N_LANG_KEY, next); } catch {}
  window.Lang.current = next;
  syncChromeI18n();
  if (window.App?.currentScreen) window.App.navigate(window.App.currentScreen, window.App.screenContext || {}, false);
}

function syncChromeI18n() {
  const label = (id, key) => {
    const el = document.getElementById(id);
    if (el) el.textContent = t(key);
  };
  label('nav-label-home', 'home');
  label('nav-label-search', 'searchNav');
  label('nav-label-post', 'post');
  label('nav-label-messages', 'messages');
  label('nav-label-profile', 'profile');
  const aria = [
    ['nav-home', 'ariaLabelHome'],
    ['nav-search', 'ariaLabelSearch'],
    ['nav-post', 'ariaLabelPost'],
    ['nav-messages', 'ariaLabelMessages'],
    ['nav-profile', 'ariaLabelProfile']
  ];
  aria.forEach(([id, key]) => {
    const el = document.getElementById(id);
    if (el) el.setAttribute('aria-label', t(key));
  });
}

window.Lang = {
  current: getLang(),
  set: setLang,
  t,
  formatLocation,
  syncChrome: syncChromeI18n
};
